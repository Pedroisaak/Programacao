/*
* atrabuição é nada mais que ao invez de voce atribuir um valor de uma variavel para outra, trabalhar matematicamente com ambas
*
 */

const a = 7
let b = 3

b += a  // b = b + a
console.log(b)

b -= 4  // b = b - 4
console.log(b)

b *= 2  // b = b * 2
console.log(b)

b /= 2  // b = b / 2
console.log(b)

b %= 2  // b = b % 2  nada mais é o resto da divisao, se o valor dividido for um valor par sera 0  caso contrario 1
console.log(b)
